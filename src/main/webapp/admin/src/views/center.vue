<script>
// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isMobile,isPhone,isURL,checkIdCard } from "@/utils/validate";

export default {
  data() {
    return {
      ruleForm: {},
      flag: '',
      usersFlag: false,
      xiaoyikeshiOptions: [],
      xiaoyixingbieOptions: [],
      yonghuxingbieOptions: [],
      yonghujiaoseOptions: [],
      yonghuxueyuanOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.$http({
      url: `option/keshi/keshi`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.xiaoyikeshiOptions = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.xiaoyixingbieOptions = "男,女".split(',')
    this.yonghuxingbieOptions = "男,女".split(',')
    this.yonghujiaoseOptions = "学生,教职工".split(',')
    this.$http({
      url: `option/xueyuan/xueyuan`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.yonghuxueyuanOptions = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
  },
  methods: {
    xiaoyitouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    onUpdateHandler() {
      if((!this.ruleForm.xiaoyigonghao)&& 'xiaoyi'==this.flag){
        this.$message.error('校医工号不能为空');
        return
      }
      if((!this.ruleForm.xiaoyixingming)&& 'xiaoyi'==this.flag){
        this.$message.error('校医姓名不能为空');
        return
      }
      if( 'xiaoyi' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
        this.$message.error(`联系电话应输入手机格式`);
        return
      }
        if(this.ruleForm.touxiang!=null) {
                this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
        }
      if((!this.ruleForm.xuehao)&& 'yonghu'==this.flag){
        this.$message.error('学号不能为空');
        return
      }
      if((!this.ruleForm.xingming)&& 'yonghu'==this.flag){
        this.$message.error('姓名不能为空');
        return
      }
      if((!this.ruleForm.mima)&& 'yonghu'==this.flag){
        this.$message.error('密码不能为空');
        return
      }
      if( 'yonghu' ==this.flag && this.ruleForm.nianling&&(!isIntNumer(this.ruleForm.nianling))){
       this.$message.error(`年龄应输入整数`);
        return
      }
      if( 'yonghu' ==this.flag && this.ruleForm.shoujihao&&(!isMobile(this.ruleForm.shoujihao))){
        this.$message.error(`手机号应输入手机格式`);
        return
      }
      if('users'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
</script>
